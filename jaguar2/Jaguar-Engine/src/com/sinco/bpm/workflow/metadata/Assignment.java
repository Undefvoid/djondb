package com.sinco.bpm.workflow.metadata;


public class Assignment implements java.io.Serializable {

    private long id;
    
    private Property assignmentTo;
    
    private Expression assignmentFrom;
    
    private AssignTime assignTime;
    
    private Expression expression;

    private CommonEvent event;
    
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public Property getAssignmentTo() {
        return assignmentTo;
    }

    public void setAssignmentTo(Property assignmentTo) {
        this.assignmentTo = assignmentTo;
    }

    public Expression getAssignmentFrom() {
        return assignmentFrom;
    }

    public void setAssignmentFrom(Expression assignmentFrom) {
        this.assignmentFrom = assignmentFrom;
    }

    public AssignTime getAssignTime() {
        return assignTime;
    }

    public void setAssignTime(AssignTime assignTime) {
        this.assignTime = assignTime;
    }

    public Expression getExpression() {
        return expression;
    }

    public void setExpression(Expression expression) {
        this.expression = expression;
    }

    public CommonEvent getEvent() {
        return event;
    }

    public void setEvent(CommonEvent event) {
        this.event = event;
    }
}